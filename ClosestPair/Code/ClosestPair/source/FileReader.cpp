//-------------------------------------------------------------------------------
// @ FileReader::getFileContents(const char * filename)
//-------------------------------------------------------------------------------
// reads the contents of a filename passed by parameters
//-------------------------------------------------------------------------------

std::string FileReader::GetFileContents(const char *filename)
{
    std::ifstream in(filename, std::ios::in | std::ios::binary);
    _ASSERT_EXPR(in, L"filename passed as input is not a valid one");

    std::string contents;
    in.seekg(0, std::ios::end);
    contents.resize((size_t)in.tellg());
    in.seekg(0, std::ios::beg);
    in.read(&contents[0], contents.size());
    in.close();

    return(contents);
}